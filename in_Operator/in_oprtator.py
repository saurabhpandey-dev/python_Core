my_str = 'Hello world'
print('Hello' in my_str)  # True

# with List
lst = ['ram','shyam',63,7.5,'sita','gita']
print(type(lst))
print('shyam' in lst) #true

# with tuple
tup = ('ram','shyam','sita','gita',63,7.5)
print(type(tup))
print(63 in tup) #true

# with set

sets = {3,6,75,3,7}
print(type(sets))
print(6 in sets)  #true
