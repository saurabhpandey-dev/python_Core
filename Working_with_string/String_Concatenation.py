##String Concatenation: To concatenate strings, you can use the + operator like
##this:
    
developer = 'Jessica'
print('My name is ' + developer + '.') # My name is Jessica.

##Another way to concatenate strings is by using the += operator. This is used
##to perform concatenation and assignment in the same step like this:

greeting = 'My name is '
developer = 'Jessica.'

greeting += developer
print(greeting) # My name is Jessica.
