##String Slicing: This is when you can extract portions of a string. Here is the
##basic syntax:
    
##    str[start:stop:step]

##The start position represents the index where the extraction should begin.
##The stop position is where the slice should end. This position is non inclusive.
##The step position represents the interval to increment for the slicing.
##Here are some examples:

message = 'Python is fun!'

print(message[0:6])  # Python
print(message[7:])  # is fun!
print(message[::2])  # Pto sfn
