#List Mutability: Lists are mutable, meaning you can update any element in the
#list as long as you pass in a valid index number. To update lists at a
#particular index, you can assign a new value to that index.

programming_languages = ['Python', 'Java', 'C++', 'Rust']
print(programming_languages)
programming_languages[0] = 'JavaScript'
print(programming_languages)
