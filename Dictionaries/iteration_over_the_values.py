products = {
    'Laptop': 990,
    'Smartphone': 600,
    'Tablet': 250,
    'Headphones': 70,
}

for product in products.values():
    print(product)

##products.values() to get all the values of the products dictionary, a colon,
##and then the body of the loop, where you can apply any logic to the values.
##In this case, we are printing them.
##
##The loop variable will take each one of the values, one per iteration.
