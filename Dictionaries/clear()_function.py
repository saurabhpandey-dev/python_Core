##The .clear() method removes all the key-value pairs from the dictionary:

pizza = {
    'name': 'Margherita Pizza',
    'price': 8.9,
    'calories_per_slice': 250,
    'toppings': ['mozzarella', 'basil']
}

print('having all data :-\n',pizza, '\n')

pizza.clear()

print('all data got deleted :- \n')
print(pizza,'\n')


