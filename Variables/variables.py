#Declaring Variables: To declare a variable, you start with the variable
#name followed by the assignment operator (=) and then the value.
#This can be a number, string, boolean, etc. Here are some examples:

name = 'John Doe'
age = 25
print(name , ' ', age)

#Naming Conventions for Variables: Here are the naming conventions you
#should use for variables:

##Variable names can only start with a letter or an underscore (_), not a number.
##Variable names can only contain alphanumeric characters (a-z, A-Z, 0-9)
##and underscores (_).
##Variable names are case-sensitive — age, Age, and AGE are all considered unique.
##Variable names cannot be one of Python’s reserved keywords such as if, class,
##or def.
##Variables names with multiple words are separated by underscores.
##Ex. snake_case.
